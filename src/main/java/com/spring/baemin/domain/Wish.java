package com.spring.baemin.domain;

public class Wish {
	
	private Long wish_no;
	private String user_id;
	private Long sotre_no;

	public Long getWish_no() {
		return wish_no;
	}
	public void setWish_no(Long wish_no) {
		this.wish_no = wish_no;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public Long getSotre_no() {
		return sotre_no;
	}
	public void setSotre_no(Long sotre_no) {
		this.sotre_no = sotre_no;
	}
	
}
